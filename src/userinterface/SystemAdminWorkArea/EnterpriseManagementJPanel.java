    public void loadtable(){
        DefaultTableModel model = (DefaultTableModel) tbl_list_of_enterprises.getModel();
        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise.getName();
                    row[1] = network.getName();
                    row[2] = enterprise.getEnterpriseType().getValue();
                    model.addRow(row);
                }
            
        }
    }
    
    private void currentEnterprises_dropdownActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_currentEnterprises_dropdownActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbl_list_of_enterprises.getModel();
        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            if (network.getName() == currentEnterprises_dropdown.getSelectedItem()) {
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise.getName();
                    row[1] = network.getName();
                    row[2] = enterprise.getEnterpriseType().getValue();
                    model.addRow(row);
                }
            }else if(currentEnterprises_dropdown.getSelectedIndex()==0){
                loadtable();
            }
        }
    }//GEN-LAST:event_currentEnterprises_dropdownActionPerformed

