    private void btn_addNewEnterpriseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addNewEnterpriseActionPerformed

        int counter = 0;
        String name = txt_Add_new_EntityName.getText();
        String city = currentEnterprise_add_new_network.getSelectedItem().toString();
        if (name.matches("^([A-Za-z]+)(\\s[A-Za-z]+)*\\s?$")) {
            name = name.trim();
            for (String s : entnet) {
                if (s.equalsIgnoreCase(name + city)) {
                    JOptionPane.showMessageDialog(null, "Please try aagain, The Enterprise already exists in this city");
                    counter++;
                    break;
                }
            }
            if (counter == 0) {
                entnet.add(name + city);
                Network network = (Network) currentEnterprise_add_new_network.getSelectedItem();
                Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) currentEnterprise_add_new_Type.getSelectedItem();

                if (network == null || type == null) {
                    JOptionPane.showMessageDialog(null, "Please try again, Invalid Input!");
                    return;
                }
                
                if (checkcurrentEnterprise(type)) {
                    return;

                } else {
                    Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(name, type);
                    
                    loadEnterpriseTable();
                    txt_Add_new_EntityName.setText("");
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Invalid input, Enterprise can only contain alphabets");
        }

    }//GEN-LAST:event_btn_addNewEnterpriseActionPerformed

