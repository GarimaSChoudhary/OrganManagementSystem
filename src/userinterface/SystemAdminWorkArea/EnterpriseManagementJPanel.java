    private void loadEnterpriseTable() {
        DefaultTableModel entTable = (DefaultTableModel) tbl_list_of_enterprises.getModel();
        entTable.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                Object[] row = new Object[3];
                row[0] = enterprise.getName();
                row[1] = network.getName();
                row[2] = enterprise.getEnterpriseType().getValue();
                entTable.addRow(row);
            }
        }
    }

    private boolean checkcurrentEnterprise(Enterprise.EnterpriseType type) {
        ArrayList<Enterprise.EnterpriseType> estEnterprises = new ArrayList<Enterprise.EnterpriseType>();
        for (Network network : system.getNetworkList()) {
            if (network == currentEnterprise_add_new_network.getSelectedItem()) {
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                    estEnterprises.add(enterprise.getEnterpriseType());
                }

            }
        }
        if (estEnterprises.contains(type)&&(type.getValue().equalsIgnoreCase("government")||type.getValue().equalsIgnoreCase("organ bank"))) {
            JOptionPane.showMessageDialog(null, "One enterprise allowed for the selected option");
            return true;
        }

        return false;
    }

    private void LoadComboBox() {
        currentEnterprise_add_new_network.removeAllItems();
        currentEnterprise_add_new_Type.removeAllItems();
        currentEnterprise_add_new_network.addItem("--Select--");
        currentEnterprise_add_new_Type.addItem("--Select--");
        currentEnterprises_dropdown.addItem("-All Enterprises-");
        for (Network network : system.getNetworkList()) {
            currentEnterprise_add_new_network.addItem(network);
            currentEnterprises_dropdown.addItem(network.toString());
        }

        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            currentEnterprise_add_new_Type.addItem(type);
        }

    }

