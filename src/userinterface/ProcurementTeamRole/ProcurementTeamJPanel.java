    private void btn_in_processActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_in_processActionPerformed

        int selectedRow = tbl_work_request.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Select request to Process", "Warining", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        OrganProcureWorkRequest orgwr = (OrganProcureWorkRequest)tbl_work_request.getValueAt(selectedRow, 0);
        boolean check;
        check=orgwr.getTeam().equalsIgnoreCase(organization.getOrgName());
                
        if(!check){
            JOptionPane.showMessageDialog(null, "This task is assigned to other team");
            return;
        }
        if(orgwr.getStatus().equals("Assigned")){
          orgwr.setStatus("InProcess");  
        }

     populate_tbl();
  
    }//GEN-LAST:event_btn_in_processActionPerformed

    private void btn_completedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_completedActionPerformed

       int selectedRow = tbl_work_request.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Select request to Complete", "Warining", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        OrganProcureWorkRequest orgwr = (OrganProcureWorkRequest)tbl_work_request.getValueAt(selectedRow, 0);
        if(!orgwr.getStatus().equals("InProcess")){
        JOptionPane.showMessageDialog(null,"Request should be InProcess", "Warining", JOptionPane.WARNING_MESSAGE);
            return;
        }
                   if(organization instanceof Facilities){
                       orgwr.setStatus("Completed");
                   ((Facilities) organization).setCounterNumber(0);}

     populate_tbl();  
    }//GEN-LAST:event_btn_completedActionPerformed


