    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        boolean check;
        check=(ped_status_comboBox.getSelectedIndex()>0&&age_comboBox.getSelectedIndex()>0&&request_organ_combo_box.getSelectedIndex()>0&&survival_benefit_comboBox.getSelectedIndex()>0&&urgency_comboBox.getSelectedIndex()>0);
        if(check == false) {
            JOptionPane.showMessageDialog(null, "Please provide all parameters");
            return;
        }

        int dup=0;
        for(Wait w: ecoSystem.getWaitList()){
            if(w.getPatient().getPatientId()==p.getPatientId()&&w.getOrgan().equalsIgnoreCase(request_organ_combo_box.getSelectedItem().toString())){
               dup++; 
            }
        }
        
        check=(dup==0);
        if(!check){
            JOptionPane.showMessageDialog(null, "Patient is already in waitlist");
            return;
        }

        int score=0;
        if(ecoSystem.getWaitList().size()<1){
            score=calculateScore();
        }else{
            Collections.sort(ecoSystem.getWaitList());
            for(Wait w: ecoSystem.getWaitList()){
                if(w.getOrgan().equalsIgnoreCase(request_organ_combo_box.getSelectedItem().toString())){
                    score=w.getWaitlist()-1;
                    break;
                }else{
                    score=calculateScore();
                }
            }
        
        }
        
        Wait waitlist_val=new Wait(p,age_comboBox.getSelectedItem().toString(),ped_status_comboBox.getSelectedItem().toString(),survival_benefit_comboBox.getSelectedItem().toString(),urgency_comboBox.getSelectedItem().toString(),request_organ_combo_box.getSelectedItem().toString(),score,0);
        ecoSystem.addWaitlist(waitlist_val);

        DoctorWorkAreaJPanel doctorWorkArea=new DoctorWorkAreaJPanel(userProcessContainer, userAccount,(HealthCare) org,enterprise,ecoSystem,network);
        doctorWorkArea.load_wait_table();
        userProcessContainer.add("Lab test",doctorWorkArea);
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btn_submitActionPerformed

